    private void setBurgerPatty(javax.swing.JComboBox<String> jc){
        if(jc.getSelectedItem().toString()=="Aloo"){
            burg.setName(burg.getName()+"Aloo Patty ");
            burg.setPrice(burg.getPrice()+10);
            burg.setNoOfPatties(burg.getNoOfPatties()+1);
        }
        else if(jc.getSelectedItem().toString()=="Chicken"){
            burg.setName(burg.getName()+"Chicken Patty ");
            burg.setPrice(burg.getPrice()+80);
            burg.setNoOfPatties(burg.getNoOfPatties()+1);
        }
        else if(jc.getSelectedItem().toString()=="Beef"){
            burg.setName(burg.getName()+"Beef Patty ");
            burg.setPrice(burg.getPrice()+100);
            burg.setNoOfPatties(burg.getNoOfPatties()+1);
        }
        else if(jc.getSelectedItem().toString()=="Cheese"){
            burg.setName(burg.getName()+"Cheese Patty ");
            burg.setPrice(burg.getPrice()+70);
            burg.setNoOfPatties(burg.getNoOfPatties()+1);
        }
    }

