    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jComboBox1.getSelectedItem().toString()=="None"){
            JOptionPane.showMessageDialog(null,"Please enter Patty 1!");
            CustomBurger cb=new CustomBurger(burg,orderList);
            cb.setVisible(true);
            dispose();
        }
        else{
            setBurgerPatty(jComboBox1);
            setBurgerPatty(jComboBox2);
            setBurgerPatty(jComboBox3);
            burg.setName(burg.getName()+"Burger");
            burg.setExtraCheese(jToggleButton1.isSelected());
            if(burg.isExtraCheese()){
                burg.setPrice(burg.getPrice()+40);
                burg.setName(burg.getName()+"(Extra Cheese)");
            }
            FinalOrder fo=new FinalOrder(burg,orderList);
            fo.setVisible(true);
            dispose();
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

